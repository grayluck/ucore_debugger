int
load_icode_read(int fd, void *buf, size_t len, off_t offset) {
    int ret;
    if ((ret = seek(fd, offset, 0)) != 0) {
        return ret;
    }
    if ((ret = read(fd, buf, len)) != len) {
        return (ret < 0) ? ret : -1;
    }
    return 0;
}

